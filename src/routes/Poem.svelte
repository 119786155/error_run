<div class="poem container">
	<h1>道德经</h1>
	<p class="author">李耳 (字伯阳) <i>(公元前571年—公元前470年)</i></p>
	<p>信言不美，美言不信。</p>
	<p>善者不辩，辩者不善。</p>
	<p>知者不博，博者不知。</p>
	<p>圣人不积，既以为人己愈有，既以与人己愈多。</p>
	<p>天之道，利而不害。</p>
	<p>圣人之道，为而不争。</p>
</div>

