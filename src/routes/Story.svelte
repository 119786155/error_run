<div class="poem container">
	<h1>兔朵朵的魔法纽扣</h1>
	<p class="author"><i>2025-09-30</i></p>
	<p>
		兔朵朵有个怪爱好 —— 收集纽扣。她的布口袋里装着圆的、方的、带花纹的纽扣，连睡觉都要抱三颗最亮的。
	</p>
	<p>
		某天傍晚，雨点 “哒哒”
		敲着树叶，兔朵朵躲进老橡树洞，忽然摸到个软乎乎的东西。是只缺了眼睛的布狐狸！它的眼眶处缝着空空的线套，看起来好可怜。
	</p>
	<p>
		“试试这个？”
		兔朵朵掏出颗红纽扣，刚缝上去，布狐狸的眼睛突然闪了闪，竟开口说话了：“谢谢你呀，小兔子！”
	</p>
	<p>
		原来布狐狸是小刺猬的旧玩具，被风吹到了树洞。兔朵朵又找出颗蓝纽扣当另一只眼睛，布狐狸立刻蹦起来，晃了晃尾巴上的破洞：“要是有颗星星纽扣补这里，我就能带你们去看萤火虫啦！”
	</p>
	<p>
		兔朵朵赶紧翻口袋，真的摸到颗闪着微光的星星纽扣！刚缝好，布狐狸的尾巴就飘了起来，带着兔朵朵飞出树洞。夜色里，萤火虫围着他们转，像撒了把星星，远处还传来小刺猬惊喜的呼喊
		—— 原来他正举着灯笼找布狐狸呢！
	</p>
	<p>后来，兔朵朵的纽扣口袋里，总留着一颗空位置，她说要给下一个需要帮助的朋友。</p>
</div>

